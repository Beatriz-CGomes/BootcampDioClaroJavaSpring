package moduloII.estruturaCondicional;

public class ExercicioSwitchCase {
    public static void main(String[] args) {
        //estudar continue, break and default
        String plano = "T";

        switch (plano) {
            case "T": {
                System.out.println("5GB Youtube");
            }
            case "M": {
                System.out.println("Whats e Instagram grátis");
            }
            case "B": {
                System.out.println("100 minutos de ligaçãi");
            }
        }
    }
}
